#include <stdio.h>
#include <unistd.h>
#include <krb/cached_time.hpp>

int main()
{
  cached_time &C = cached_time::init();

  // to verify the caching is doing what it's supposed to, do an
  // strace: strace ./time 2>&1 |grep -v nanosleep
  while(1) {
    usleep(1000);
    C.time();
  }

  return 0;
}
